package databeans;

public class CheckBean {
	private int cid;
	private double amount;
	private double cash;
    	
	public int getCID () {
		return cid;
	}
	
	public void setCID (int cid) {
		this.cid = cid;
	}
	
	public double getCash () {
		return cash;
	}
	
	public void setCash (double cid) {
		this.cash = cid;
	}
	
	public double getAmount () {
		return amount;
	}
	
	public void setAmount (double amount) {
		this.amount = amount;
	}
}
